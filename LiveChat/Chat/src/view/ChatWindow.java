    private void loadchat() {
        ArrayList<String> msglist = (ArrayList<String>) client.receive();
        int ncount = msglist.size();
        for (String m : msglist) {
            System.out.println(m);
        }
        if (chatoldcount != ncount) {
            msgpanel.setText("");
            StyledDocument doc = msgpanel.getStyledDocument();
            Style style = msgpanel.addStyle("ok", null);
            StyleConstants.setForeground(style, Color.blue);
            StyleConstants.setFontSize(style, 10);
            StyleConstants.setSpaceAbove(style, 4);

            for (String ml : msglist) {
                String[] data = ml.split("#");
                String thread = data[3];
                /*check if all messages are for this chat*/
                if (thread.equals(SelectThreadPage.threadname)) {
                    /*check if message comes from this user*/
                    if (data[0].equals(ChatLogin.loginid)) {
                        try {
                            //doc.insertString(doc.getLength(), "send by " + data[0] + " at " + data[2] + "\n", style);
                            StyleConstants.setForeground(style, Color.GREEN);
                            StyleConstants.setFontSize(style, 14);
                            StyleConstants.setBold(style, true);
                            doc.insertString(doc.getLength(), data[1] + "\n\n", style);
                            StyleConstants.setForeground(style, Color.BLUE);
                            StyleConstants.setFontSize(style, 10);
                            StyleConstants.setBold(style, false);
                            //StyleConstants.setForeground(style, Color.red);
                            // msgpanel.append("send by " + data[0] + " at " + data[2] + "\n" + data[1] + "\n\n");
                            //doc.insertString(doc.getLength(),"send by "+data[0], style);
                        } catch (BadLocationException ex) {
                            Logger.getLogger(ChatWindow.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    } else {/*message is from diffrent user*/
                        try {
                            StyleConstants.setForeground(style, Color.RED);
                            doc.insertString(doc.getLength(), "send by " + data[0] + " at " + data[2] + "\n", style);
                            StyleConstants.setForeground(style, Color.blue);
                            StyleConstants.setFontSize(style, 14);
                            StyleConstants.setBold(style, true);
                            doc.insertString(doc.getLength(), data[1] + "\n\n", style);
                            //StyleConstants.setForeground(style, Color.red);
                            StyleConstants.setFontSize(style, 10);
                            StyleConstants.setBold(style, false);

                        } catch (BadLocationException ex) {
                            Logger.getLogger(ChatWindow.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }
            chatoldcount = ncount;

        }

    }

