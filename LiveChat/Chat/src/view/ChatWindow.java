    private void updateUI() {
        c1 = new GridLayout(1, 0);

        updateThread = new Thread(new Runnable() {
            @Override
            public void run() {
                while (isrun) {
                    try {
                        Thread.sleep(500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(ChatWindow.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    showActiveUsers();
                    loadchat();
                }
            }

        });
        updateThread.start();
    }

    public void showActiveUsers() {
        ArrayList<String> activelist = (ArrayList<String>) client.getActivelist();

        int count = activelist.size();
        if (oldcount != count) {
            activepanel.removeAll();
            activepanel.revalidate();
            activepanel.repaint();

            if ((activelist.size() > 0)) {
                for (String al : activelist) {
                    String[] data = al.split("#");
                    JLabel b = new JLabel(data[0] + "");
                    b.setBounds(20, rows * 20, 100, 20);
                    b.setForeground(Color.GREEN);

                    b.setFont(new Font("Serif", Font.BOLD, 16));
                    activepanel.add(b);
                    rows++;
                }

            }
            oldcount = count;
            rows = 2;
        }
    }

